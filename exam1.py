t_base = float(input("Enter the base of triangle:"))
t_height = float(input("Enter the height of triangle:"))
Triangle_area = (t_base*t_height)/2
print("Area of a triangle:", Triangle_area)
r_length = float(input("Enter the length of rectangle:"))
r_breadth = float(input("Enter the breadth of rectangle:"))
Rectangle_area = r_length*r_breadth
print("Area of a rectangle:", Rectangle_area)
c_radius = float(input("Enter the radius of circle:"))
Circle_area = 3.141 * c_radius * c_radius
print("Area of Circle=", Circle_area)